const header = document.querySelector(".site-header");
const navToggle = document.querySelector(".nav-toggle");
const navMenu = document.querySelector(".nav-menu");
const languageToggle = document.querySelector("[data-language-toggle]");
const languageTarget = document.querySelector("[data-language-target]");
const navLinks = Array.from(document.querySelectorAll(".nav-link"));
const sections = navLinks
  .map((link) => document.querySelector(link.getAttribute("href")))
  .filter(Boolean);
let ticking = false;
let currentLanguage = "en";

const setMetaContent = (selector, value) => {
  const element = document.querySelector(selector);
  if (element) {
    element.setAttribute("content", value);
  }
};

const updateUrlLanguage = (language) => {
  const url = new URL(window.location.href);
  if (language === "zh") {
    url.searchParams.set("lang", "zh");
  } else {
    url.searchParams.delete("lang");
  }
  window.history.replaceState(null, "", `${url.pathname}${url.search}${url.hash}`);
};

const applyLanguage = (language, { persist = true, updateUrl = true } = {}) => {
  currentLanguage = language === "zh" ? "zh" : "en";
  const dictionary = translations[currentLanguage];

  document.documentElement.lang = currentLanguage === "zh" ? "zh-CN" : "en";
  document.title = dictionary["meta.title"];
  setMetaContent('meta[name="description"]', dictionary["meta.description"]);
  setMetaContent('meta[property="og:title"]', dictionary["meta.title"]);
  setMetaContent('meta[property="og:description"]', dictionary["meta.ogDescription"]);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const value = dictionary[element.dataset.i18nHtml];
    if (value) {
      element.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const value = dictionary[element.dataset.i18nAria];
    if (value) {
      element.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const value = dictionary[element.dataset.i18nTitle];
    if (value) {
      element.setAttribute("title", value);
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = dictionary[element.dataset.i18nAlt];
    if (value) {
      element.setAttribute("alt", value);
    }
  });

  if (languageToggle && languageTarget) {
    languageTarget.textContent = dictionary["language.target"];
    languageToggle.setAttribute("aria-label", dictionary["language.title"]);
    languageToggle.setAttribute("title", dictionary["language.title"]);
  }

  if (persist) {
    localStorage.setItem("homepage-language", currentLanguage);
  }
  if (updateUrl) {
    updateUrlLanguage(currentLanguage);
  }

  setActiveLink();
};

const getInitialLanguage = () => {
  const params = new URLSearchParams(window.location.search);
  const requested = params.get("lang");
  if (requested === "zh" || requested === "en") {
    return requested;
  }

  const saved = localStorage.getItem("homepage-language");
  if (saved === "zh" || saved === "en") {
    return saved;
  }

  return navigator.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
};

const setHeaderState = () => {
  if (!header) return;
  header.classList.toggle("scrolled", window.scrollY > 18);
};

function setActiveLink() {
  if (!header || sections.length === 0) return;

  const offset = window.scrollY + header.offsetHeight + 120;
  let currentId = sections[0].id;

  sections.forEach((section) => {
    if (section.offsetTop <= offset) {
      currentId = section.id;
    }
  });

  navLinks.forEach((link) => {
    link.classList.toggle("active", link.getAttribute("href") === `#${currentId}`);
  });
}

const closeMenu = () => {
  if (!navMenu || !navToggle) return;
  navMenu.classList.remove("open");
  navToggle.setAttribute("aria-expanded", "false");
  document.body.classList.remove("menu-open");
};

const onScroll = () => {
  setHeaderState();

  if (!ticking) {
    window.requestAnimationFrame(() => {
      setActiveLink();
      ticking = false;
    });
    ticking = true;
  }
};

if (languageToggle) {
  languageToggle.addEventListener("click", () => {
    applyLanguage(currentLanguage === "zh" ? "en" : "zh");
  });
}

if (navToggle && navMenu) {
  navToggle.addEventListener("click", () => {
    const isOpen = navMenu.classList.toggle("open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
    document.body.classList.toggle("menu-open", isOpen);
  });
}

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    closeMenu();
    navLinks.forEach((item) => item.classList.remove("active"));
    link.classList.add("active");
  });
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeMenu();
  }
});

applyLanguage(getInitialLanguage(), { persist: false, updateUrl: false });
setHeaderState();
setActiveLink();
window.addEventListener("scroll", onScroll, { passive: true });
window.addEventListener("hashchange", setActiveLink);
